import { EntitySchema } from 'typeorm';
import { Brand } from './brand.entity';

export const BrandSchema = new EntitySchema<unknown>({
  name: 'Brand',
  target: Brand,
  columns: {
    id: {
      type: Number,
      primary: true,
      generated: true,
    },
    firstName: {
      type: String,
    },
    lastName: {
      type: String,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  relations: {
    photos: {
      type: 'one-to-many',
      target: 'Photo', // the name of the PhotoSchema
    },
  },
});